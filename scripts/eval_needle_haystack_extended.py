def main():
    parser = argparse.ArgumentParser(
        description="Extended Needle-in-Haystack evaluation for RMT v10",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Single-needle at extended lengths
  python scripts/eval_needle_haystack_extended.py \\
    --base_model ../models/Qwen--Qwen3-8b \\
    --checkpoint outputs/rmt_v10_xxx/final/ \\
    --modes single \\
    --lengths 4096 8192 16384 32768 \\
    --output_dir outputs/nih_extended/

  # Multi-needle stress test
  python scripts/eval_needle_haystack_extended.py \\
    --base_model ../models/Qwen--Qwen3-8b \\
    --checkpoint outputs/rmt_v10_xxx/final/ \\
    --modes multi \\
    --lengths 8192 16384 \\
    --multi_needles 3 5 \\
    --num_trials 2

  # Full eval (single + multi)
  python scripts/eval_needle_haystack_extended.py \\
    --base_model ../models/Qwen--Qwen3-8b \\
    --checkpoint outputs/rmt_v10_xxx/final/ \\
    --modes single multi \\
    --lengths 4096 8192 16384 \\
    --multi_needles 3 5
""")
    parser.add_argument("--base_model", type=str, required=True)
    parser.add_argument("--checkpoint", type=str, default=None,
                        help="RMT checkpoint dir. Required unless --base_only.")
    parser.add_argument("--output_dir", type=str, default="outputs/nih_extended/")
    parser.add_argument("--modes", nargs="+", default=["single", "multi"],
                        choices=["single", "multi"],
                        help="Evaluation modes to run")
    parser.add_argument("--depths", type=float, nargs="+",
                        default=[0.1, 0.25, 0.5, 0.75, 0.9],
                        help="Needle insertion depths (single mode)")
    parser.add_argument("--lengths", type=int, nargs="+",
                        default=[4096, 8192, 16384, 32768],
                        help="Context lengths in tokens")
    parser.add_argument("--num_trials", type=int, default=3,
                        help="Number of random trials per config")
    parser.add_argument("--multi_needles", type=int, nargs="+",
                        default=[3, 5],
                        help="Number of needles for multi-needle mode")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--lang", type=str, default="en", choices=["en", "zh"])
    parser.add_argument("--max_new_tokens", type=int, default=30,
                        help="Max tokens to generate for answer")
    parser.add_argument("--base_only", action="store_true",
                        help="Only evaluate base model (skip RMT loading)")
    args = parser.parse_args()
    if not args.base_only and not args.checkpoint:
        parser.error("--checkpoint is required unless --base_only is set")

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    os.makedirs(args.output_dir, exist_ok=True)

    logger.info("Loading base model ...")
    base_model, tokenizer = load_base_model(args.base_model, device)

    rmt_model = None
    rmt_config = None
    model_label = "rmt_v10"
    rmt_max_context = 0

    if not args.base_only:
        logger.info("Loading RMT v10 model ...")
        rmt_model, rmt_tokenizer, rmt_config = load_rmt_v10_model(args.base_model, args.checkpoint, device)

        segment_length = rmt_config["segment_length"]
        max_segments = rmt_config["max_segments"]
        rmt_max_context = segment_length * max_segments

        if rmt_config.get("use_l2"):
            model_label += "_l2"

        logger.info(f"Config: base_max={getattr(base_model.config, 'max_position_embeddings', 'unknown')} "
                    f"rmt_seg={segment_length} rmt_max_segs={max_segments} "
                    f"rmt_max_context={rmt_max_context}")

        # Warn about lengths exceeding RMT capacity
        for l in args.lengths:
            if l > rmt_max_context:
                logger.warning(f"Length {l} exceeds RMT capacity {rmt_max_context} "
                               f"({max_segments} segments × {segment_length}). "
                               f"RMT will only process last {rmt_max_context} tokens. "
                               f"Needles in earlier positions will be lost.")
    else:
        rmt_config = {"segment_length": 1024, "max_segments": 4}
        logger.info("Base-only mode: skipping RMT loading")

    logger.info(f"Modes: {args.modes}, Lengths: {args.lengths}, "
                f"Depths: {args.depths}, Multi-needles: {args.multi_needles}")

    all_results = []

    if "single" in args.modes:
        logger.info("=" * 60)
        logger.info("Running SINGLE-NEEDLE evaluation")
        logger.info("=" * 60)
        results = run_single_needle_eval(args, base_model, tokenizer, rmt_model, rmt_config)
        all_results.extend(results)

    if "multi" in args.modes:
        logger.info("=" * 60)
        logger.info("Running MULTI-NEEDLE evaluation")
        logger.info("=" * 60)
        results = run_multi_needle_eval(args, base_model, tokenizer, rmt_model, rmt_config)
        all_results.extend(results)

    # ─── Summary ───
    summary = compute_summary(all_results, model_label)

    if "single" in args.modes:
        format_single_results(all_results, args, model_label)
    if "multi" in args.modes:
        format_multi_results(all_results, args, model_label)

    # Print summary
    print("\n" + "=" * 60)
    print("SUMMARY")
    print("=" * 60)
    for key, val in sorted(summary.items()):
        print(f"  {key}: {val:.1%}")
    print("=" * 60)

    # Save results
    output = {
        "timestamp": datetime.now().isoformat(),
        "args": {k: v for k, v in vars(args).items()},
        "rmt_config": rmt_config,
        "rmt_max_context": rmt_max_context,
        "summary": summary,
        "results": all_results,
    }
    out_path = os.path.join(args.output_dir, "nih_extended_results.json")
    with open(out_path, "w") as f:
        json.dump(output, f, indent=2, ensure_ascii=False)
    logger.info(f"Results saved to {out_path}")


if __name__ == "__main__":
    main()
