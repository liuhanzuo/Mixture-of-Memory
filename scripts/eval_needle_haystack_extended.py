"""
Needle-in-Haystack Extended Evaluation for RMT v10.

Tests retrieval at extended context lengths (8K, 16K, 32K) where the base model
is expected to degrade, and multi-needle scenarios (2-5 needles) to stress-test
memory capacity.

Modes:
  single  — one needle per document, varying depth (standard NiH)
  multi   — N needles scattered at different depths, ask about each

Design rationale:
  - Current NIH eval maxes at 4K → base model gets 100%, uninformative.
  - At 8K/16K/32K, base model's "lost in the middle" effect appears.
  - RMT compresses long-range information into memory tokens, potentially
    maintaining retrieval accuracy where base model degrades.
  - Multi-needle tests memory capacity: can RMT store and retrieve multiple
    distinct facts simultaneously?
"""

